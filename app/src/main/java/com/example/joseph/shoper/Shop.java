public class Shop {
    private String shopName;
    private int shopBackground;
    private int shopImage;

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public int getShopBackground() {
        return shopBackground;
    }

    public void setShopBackground(int shopBackground) {
        this.shopBackground = shopBackground;
    }

    public int getShopImage() {
        return shopImage;
    }

    public void setShopImage(int shopImage) {
        this.shopImage = shopImage;
    }
}
